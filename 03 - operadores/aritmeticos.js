let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

//Incrementar
console.log(++x); //Irá aparecer 11
console.log(x++); //Irá aparecer 10, mas se chamar o log de novo irá aparecer 11

//Decrementar
console.log(--x); //Irá aparecer 9
console.log(x--); //Irá aparecer 10, mas se chamar o log de novo irá aparecer 9